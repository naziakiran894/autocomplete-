export default function CustomizedHook() {
  const handleInputChange = (event) => {
    const inputValue = event.target.value;
    const hasMathSign = /[+\-*/=<>]/.test(inputValue);

    if (hasMathSign) {
      event.target.value = "";
    }
  };

  const {
    getRootProps,
    getInputLabelProps,
    getInputProps,
    getTagProps,
    getListboxProps,
    getOptionProps,
    groupedOptions,
    value,
    focused,
    setAnchorEl,
  } = useAutocomplete({
    id: "customized-hook-demo",
    defaultValue: [top100Films[1]],
    multiple: true,
    freeSolo: true,
    options: top100Films,
    getOptionLabel: (option) => option.title,
  });

  console.log(groupedOptions, value, "dfdf");

  return (
    <Root>
      <div {...getRootProps()}>
        <Label
          style={{
            textAlign: "center",
            fontSize: "20px",
            margin: "auto",
            fontWeight: 900,
            marginTop: "20px",
          }}
          {...getInputLabelProps()}
        >
          Autocomplete Calculator
        </Label>
        <InputWrapper
          style={{ width: "70%", margin: "auto", marginTop: "20px" }}
          ref={setAnchorEl}
          className={focused ? "focused" : ""}
        >
          {value.map((option, index) => (
            <div>
              {typeof option === "object" ? (
                <StyledTag label={option.title} {...getTagProps({ index })} />
              ) : (
                <span>{option}</span>
              )}
            </div>
          ))}
          <input {...getInputProps({ onChange: handleInputChange })} />
        </InputWrapper>
      </div>
      {groupedOptions.length > 0 ? (
        <Listbox {...getListboxProps()}>
          {groupedOptions.map((option, index) => (
            <li {...getOptionProps({ option, index })}>
              <span>{option.title}</span>
              <CheckIcon fontSize="small" />
            </li>
          ))}
        </Listbox>
      ) : null}
    </Root>
  );
}

